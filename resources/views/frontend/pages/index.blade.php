                                             <h2>Types of SEO</h2>

                                             <p> There are more than 200 factors a search engine like Google perceive in
                                                 order
                                                 to judge a webpage and there are certain guidelines for optimizing
                                                 those
                                                 metrics.</p>

                                             <p> Based on the implementation, any SEO technique can be categorized into
                                                 two
                                                 parts:</p>


                                             <p>
                                             <br>
                                                 <h3>Black Hat SEO:</h3>
                                                 <br>

                                                 <p> Black Hat SEO is the process of optimizing &amp; ranking your
                                                     website through
                                                     various methods, processes &amp; techniques that violate search
                                                     engines’
                                                     guidelines.</p>
                                             </p>

                                             <p>
                                             <br>
                                                 <h3>White Hat SEO:</h3>

                                                 <p> White Hat SEO is the process of optimizing &amp;  ranking your
                                                     website through
                                                     various methods, processes &amp;  techniques according to the search
                                                     engines’
                                                     guidelines.</p>
                                             </p>

                                             <p>Note: We recommend White Hat SEO for legit businesses since they ensure
                                                 your
                                                 business in the long run. On contrary to this, Black Hat may help you
                                                 rank
                                                 fast &amp; instant but sooner or later, you will get caught from search
                                                 engines
                                                 and ultimately get penalized, resulting in at the end of your business on
                                                 the
                                                 web.</p>

<br>
