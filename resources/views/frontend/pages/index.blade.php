                                                         <h3
                                                             style="padding: 14px; background-color: #003e5e; text-align: center;">
                                                             <font color="#fff" face="PT Sans, arial"> On-Page SEO
                                                             </font>
                                                         </h3>


                                                         <!-- <h2>On Page SEO</h2> -->

                                                         <p>On-page SEO is the process of implementing various
                                                             optimization
                                                             techniques and strategies within the website itself. This
                                                             means
                                                             adjusting certain elements on your website so that search
                                                             engines are able to crawl, access and understand the
                                                             content
                                                             and structure of your website. The more relevant
                                                             information
                                                             and data a search engine can get from your webpage, the
                                                             greater
                                                             are your chances of ranking higher in their index.
                                                         </p>
                                                         <p>Identifying, working and fixing potential issues related to
                                                             On
                                                             page SEO is the first step that should be considered in
                                                             order
                                                             to optimize your website for search engines.
                                                         </p>


                                                         <h3>Elements Of On-Page SEO</h3>
                                                         <p> These are some of the most important elements of On-page
                                                             SEO:
                                                         </p>

                                                         <div style="padding-left: 41px;">


                                                             <h3>&bull; Keyword Research:</h3>

                                                             <p>More than 3.5 billion searches are conducted each day on
                                                                 Google
                                                                 to find numerous things on the web. It is therefore
                                                                 quite
                                                                 important to identify your preferred audience and
                                                                 research
                                                                 keywords accordingly.

                                                                 <p>Keyword research is simply the process of
                                                                     identifying
                                                                     relevant &amp; profitable keywords for your
                                                                     business. As a
                                                                     business owner, you must be aware of your potential
                                                                     audience and what words they use to find products
                                                                     or
                                                                     services related to your business.</p>

<br>
                                                                 <h3>&bull; Website Architecture:</h3>


                                                                 <p>In order to rank your website on the top of the search
                                                                     result, you
                                                                     must format &amp; structure your website in such a
                                                                     way that it
                                                                     becomes easily accessible to both users &amp; search
                                                                     engines.</p>

                                                                     <br>
                                                                 <h3>&bull; Meta setup:</h3>


                                                                 <p> Once you have conducted solid keyword research,
                                                                     it’s now
                                                                     time to place those keywords at relevant places
                                                                     called Meta
                                                                     tags. These are tags that provide data about your
                                                                     web page to
                                                                     search engines and website visitors. In short, they
                                                                     make it
                                                                     easier for search engines to determine what your
                                                                     content is
                                                                     about.</p>

                                                                   <P>  Meta tags can be used in:</p>
                                                                        <ul>
                                                                        <li>  <p>  &bull; Meta Title</li>  
                                                                   <li> <p> &bull; Meta Description</li>   
                                                                   <li> <p> &bull; Heading tag</li>  
                                                                   <li> <p> &bull; Alt tag</li>   </p>
                                                                   </ul>


                                                                     <br>
                                                                 <h3>&bull; Optimized Contents: </h3>


                                                                 <p> Optimized contents are the pillar of SEO. You can’t
                                                                     rank a
                                                                     webpage unless it has compelling and SEO optimized
                                                                     content.
                                                                     Just think from a user perspective. Would you visit a
                                                                     website
                                                                     that has contents of no value? Of course not. It
                                                                     is, therefore,
                                                                     an essential part of SEO to craft keyword
                                                                     optimized,
                                                                     value-adding &amp; fresh contents for your user.</p>

                                                                     <br>
                                                                 <h3>&bull; Accessibility:</h3>


                                                                 <p>All the above strategies are of no use if your
                                                                     website is not
                                                                     accessed &amp; crawled by the search engine. Make
                                                                     sure your website
                                                                     has a good navigation structure, relevant contents
                                                                     and not
                                                                     blocked by search engine bots. Make sure to
                                                                     configure these
                                                                     settings in Search console by setting up Robots.txt
                                                                     &amp; sitemap
                                                                     file.</p>
                                                                     <br>

                                                                 <h3>&bull; URL Structure:</h3>


                                                                 <p>A properly optimized URL structure defines an
                                                                     important role in
                                                                     keyword ranking. Since it is the URL that gets
                                                                     indexed first
                                                                     by a search engine, it is therefore always
                                                                     recommended to use
                                                                     an optimized URL structure. A good URL structure
                                                                     would look
                                                                     something like this
                                                                     http://digitalterai.com/seo-nepal/ </p>

                                                                 <p>
                                                                 <br> <h3>&bull; Analytics &amp; search console:</h3>
                                                                 </p>

                                                                 <p> Analytics helps you measure the performance of your
                                                                     website. It
                                                                     gives an idea about your traffic, their locations,
                                                                     devices &
                                                                     all. Search console provides you with the data
                                                                     &amp; information on
                                                                     keyword ranking, errors &amp; all.</p>


                                                                     <br> <h3>&bull; Load speed:</h3>


                                                                 <p> Nobody is going to wait for your website to load
                                                                     forever. A
                                                                     study suggests that a user normally bounce back
                                                                     from your
                                                                     website if it takes more than 3 seconds to load.
                                                                 </p>
                                                         </div>
                                                         <p>

                                                         <br>
